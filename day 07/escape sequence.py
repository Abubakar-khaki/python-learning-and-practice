# type of escape sequence
print("abubakar \n khaki")# \n use for new line
# in result we see that after \n the text is printed in new line
print("abubakar \t khaki") # \t use for tab space
# in result we see that after \t there is a tab space between abubakar and khaki
print("12345abubakar\rkhaki") # \r if use where \r after this text move to frant of line and replace text
print("abubakar\bkhaki") # \b use for backspace  
print("abubakar\fkhaki") # \f use for form feed and it is used to move the cursor to the next line and also to the beginning of the line    
print("abubakar\\khaki") # \\ use for backslash
print("abubakar \'khaki\'") # \' use for single quote
print("abubakar \"khaki\"") # \" use for double quote

