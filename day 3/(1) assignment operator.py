# assignment operator =, +=, -=, *=, /=.
a = 10 # Using assignment operator =
b = 5 # Using assignment operator =
c = 20 #for testing other operators
c += b # Using += operator, equivalent to c = c + b
print("Value of c after += b:", c)
c -= b # Using -= operator, equivalent to c = c - b
print("Value of c after -= b:", c)  
c *= b # Using *= operator, equivalent to c = c * b    
print("Value of c after *= b:", c)
c /= b # Using /= operator, equivalent to c = c / b
print("Value of c after /= b:", c)

c += 5 # even we can use any value drictly with assignment operators
print ("value after c += 5:", c)

c = c + 5 # i think this is more clear to understand
print ("value after c = c + 5:", c)
