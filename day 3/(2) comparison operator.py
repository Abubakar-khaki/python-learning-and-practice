# camparison operator ==, !=, <, >, <=, >=
a = 10
b = 20
c = 5 + 5
print("a == b:",a == b) # == use to check equality
print("a != b:", a != b) # != use for check not equality
print("a < b:", a < b) # < use to check less then
print("a > b:",a > b) # > use to check greater then 
print("a <= b:", a<= b) # <= use to check less then or equal
print("a >= c:", a >= c) # >= use to check greater then or equal


# example of comparison operator in if else
age = 19 # age variable
if age >= 18: # check if age is greater than or equal to 18
    print("You are eligible to vote.") # if condition is true
else: # else condition
    print("You are not eligible to vote.") # if condition is false
